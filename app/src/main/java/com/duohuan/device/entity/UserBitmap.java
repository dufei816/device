public class UserBitmap {

    private Bitmap positive;
    private Bitmap side;

    public void recycles() {
        if (positive != null) {
            positive.recycle();
            positive = null;
        }
        if (side != null) {
            side.recycle();
            side = null;
        }
    }

    public Bitmap getPositive() {
        return positive;
    }

    public void setPositive(Bitmap positive) {
        this.positive = positive;
    }

    public Bitmap getSide() {
        return side;
    }

    public void setSide(Bitmap side) {
        this.side = side;
    }
}
